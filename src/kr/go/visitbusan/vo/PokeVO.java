package kr.go.visitbusan.vo;

public class PokeVO {
	private String pokeId;
	private String pokedBy;
	private String visitId;
	private String visitTitle;
	private String visitAddr;
	
	public String getPokeId() {
		return pokeId;
	}
	public void setPokeId(String pokeId) {
		this.pokeId = pokeId;
	}
	public String getPokedBy() {
		return pokedBy;
	}
	public void setPokedBy(String pokedBy) {
		this.pokedBy = pokedBy;
	}
	public String getVisitId() {
		return visitId;
	}
	public void setVisitId(String visitId) {
		this.visitId = visitId;
	}
	public String getVisitTitle() {
		return visitTitle;
	}
	public void setVisitTitle(String visitTitle) {
		this.visitTitle = visitTitle;
	}
	public String getVisitAddr() {
		return visitAddr;
	}
	public void setVisitAddr(String visitAddr) {
		this.visitAddr = visitAddr;
	}
}
